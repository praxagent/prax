# PDF (.pdf)
# ---------------------------------------------------------------------------

@tool
def create_pdf(title: str, content_markdown: str) -> str:
    """Create a PDF document from markdown content.

    Args:
        title: Document title (used as filename and header).
        content_markdown: The document content in markdown format.
            Supports: headings, bold, italic, bullet lists, numbered lists, tables.

    Returns: Path to the saved .pdf file in the workspace.
    """
    try:
        from fpdf import FPDF
    except ImportError:
        return (
            "fpdf2 is not installed. Add it to pyproject.toml: "
            "uv add fpdf2"
        )

    try:
        pdf = FPDF()
        pdf.set_auto_page_break(auto=True, margin=20)
        pdf.add_page()

        # Title
        pdf.set_font("Helvetica", "B", 22)
        pdf.cell(0, 15, title, new_x="LMARGIN", new_y="NEXT", align="C")
        pdf.ln(8)

        # Parse and render markdown line by line
        lines = content_markdown.split("\n")

        for line in lines:
            stripped = line.strip()

            if not stripped:
                pdf.ln(4)
                continue

            # Headings
            if stripped.startswith("### "):
                pdf.ln(4)
                pdf.set_font("Helvetica", "B", 13)
                pdf.cell(0, 8, stripped[4:], new_x="LMARGIN", new_y="NEXT")
                pdf.ln(2)
                continue
            if stripped.startswith("## "):
                pdf.ln(5)
                pdf.set_font("Helvetica", "B", 15)
                pdf.cell(0, 9, stripped[3:], new_x="LMARGIN", new_y="NEXT")
                pdf.ln(2)
                continue
            if stripped.startswith("# "):
                pdf.ln(6)
                pdf.set_font("Helvetica", "B", 18)
                pdf.cell(0, 10, stripped[2:], new_x="LMARGIN", new_y="NEXT")
                pdf.ln(3)
                continue

            # Horizontal rule
            if stripped in ("---", "***", "___"):
                pdf.ln(3)
                pdf.line(pdf.get_x(), pdf.get_y(), pdf.get_x() + 170, pdf.get_y())
                pdf.ln(3)
                continue

            # Bullet lists
            if stripped.startswith("- ") or stripped.startswith("* "):
                pdf.set_font("Helvetica", "", 11)
                text = stripped[2:]
                text = _render_inline_formatting(text)
                pdf.cell(8)  # indent
                pdf.cell(5, 6, "\u2022")
                pdf.multi_cell(0, 6, text)
                continue

            # Numbered lists
            numbered_match = re.match(r"^(\d+)\.\s+(.+)", stripped)
            if numbered_match:
                pdf.set_font("Helvetica", "", 11)
                num = numbered_match.group(1)
                text = _render_inline_formatting(numbered_match.group(2))
                pdf.cell(8)  # indent
                pdf.cell(8, 6, f"{num}.")
                pdf.multi_cell(0, 6, text)
                continue

            # Regular paragraph
            pdf.set_font("Helvetica", "", 11)
            text = _render_inline_formatting(stripped)
            pdf.multi_cell(0, 6, text)

        # Save to bytes buffer then to workspace
        buf = BytesIO()
        pdf.output(buf)
        buf.seek(0)

        filename = f"{_slugify(title)}.pdf"
        uid = _get_user_id()
        path = workspace_service.save_file(uid, filename, buf.read())
        logger.info("Created PDF: %s", path)
        return f"PDF saved: {path}"

    except Exception as exc:
        logger.warning("Failed to create PDF: %s", exc, exc_info=True)
        return f"Failed to create PDF: {exc}"


def _render_inline_formatting(text: str) -> str:
    """Strip markdown inline formatting for plain-text PDF rendering.

    fpdf2 doesn't support rich inline formatting in multi_cell, so we
    strip bold/italic markers and return plain text.
    """
    # Remove bold (**text** or __text__)
    text = re.sub(r"\*\*(.+?)\*\*", r"\1", text)
    text = re.sub(r"__(.+?)__", r"\1", text)
    # Remove italic (*text* or _text_)
    text = re.sub(r"\*(.+?)\*", r"\1", text)
    text = re.sub(r"(?<!\w)_(.+?)_(?!\w)", r"\1", text)
    # Remove inline code (`text`)
    text = re.sub(r"`(.+?)`", r"\1", text)
    # Remove links [text](url) → text
    text = re.sub(r"\[(.+?)\]\(.+?\)", r"\1", text)
    return text


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def build_office_tools() -> list:
    """Return all office document export tools."""
    return [create_presentation, create_spreadsheet, create_pdf]
